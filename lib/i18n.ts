/** Default locale for server components (RTL Hebrew). */
export const defaultLocale: Locale = 'he';

export function getT(locale: Locale = defaultLocale): (typeof messages)[Locale] {
  return messages[locale];
}

/** Format date as DD/MM/YYYY HH:mm:ss in local timezone (for Purchase Times and report timestamps). */
export function formatDateTimeLocal(date: Date | string): string {
  const d = typeof date === 'string' ? new Date(date) : date;
  if (Number.isNaN(d.getTime())) return '—';
  const dd = String(d.getDate()).padStart(2, '0');
  const mm = String(d.getMonth() + 1).padStart(2, '0');
  const yyyy = d.getFullYear();
  const hh = String(d.getHours()).padStart(2, '0');
  const min = String(d.getMinutes()).padStart(2, '0');
  const ss = String(d.getSeconds()).padStart(2, '0');
  return `${dd}/${mm}/${yyyy} ${hh}:${min}:${ss}`;
}
